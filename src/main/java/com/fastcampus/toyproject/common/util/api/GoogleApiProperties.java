package com.fastcampus.toyproject.common.util.api;

import lombok.Getter;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.boot.context.properties.ConstructorBinding;

@Getter
@ConfigurationProperties(prefix = "spring.google-api")
@ConstructorBinding
public class GoogleApiProperties {

    private final String baseUrl;
    private final String key;

    public GoogleApiProperties(String baseUrl, String key) {
        this.baseUrl = baseUrl;
        this.key = key;
        System.out.println(this.getBaseUrl());
    }
}
